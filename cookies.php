  <h1>Politique de cookies</h1>

  <h2>Qu'est-ce qu'un cookie?</h2>
  <p>Un cookie est un petit fichier texte stocké sur votre ordinateur ou votre appareil mobile lorsque vous visitez un site web. Il permet au site web de se souvenir de vos actions et préférences (telles que la connexion, la langue, la taille de la police et d'autres préférences d'affichage) sur une période donnée, afin que vous n'ayez pas à les saisir à chaque fois que vous revenez sur le site ou naviguez d'une page à une autre.</p>

  <h2>Utilisation des cookies</h2>
  <p>Nous utilisons des cookies pour diverses raisons, notamment :</p>
  <ul>
    <li>Pour assurer le bon fonctionnement du site web.</li>
    <li>Pour analyser l'utilisation du site et améliorer son contenu et sa convivialité.</li>
    <li>Pour personnaliser votre expérience en mémorisant vos préférences et vos paramètres.</li>
    <li>Pour afficher des publicités ciblées en fonction de vos intérêts.</li>
  </ul>

  <h2>Types de cookies utilisés</h2>
  <p>Nous utilisons les types de cookies suivants :</p>
  <ul>
    <li>Cookies de session : ces cookies sont temporaires et sont supprimés lorsque vous fermez votre navigateur ou quittez le site web.</li>
    <li>Cookies persistants : ces cookies restent sur votre appareil pendant une période déterminée ou jusqu'à ce que vous les supprimiez manuellement.</li>
    <li>Cookies internes : ces cookies sont définis par le site web que vous visitez et ne peuvent être lus que par ce site.</li>
    <li>Cookies tiers : ces cookies sont définis par des domaines autres que le site que vous visitez. Nous utilisons des services tiers qui peuvent également définir des cookies sur votre appareil.</li>
  </ul>

  <h2>Contrôle des cookies</h2>
  <p>Vous pouvez contrôler et gérer les cookies en fonction de vos préférences. La plupart des navigateurs vous permettent de refuser ou d'accepter les cookies, ainsi que de supprimer les cookies existants. Veuillez consulter les paramètres de votre navigateur pour plus d'informations sur la gestion des cookies.</p>

  <h2>Modification de notre politique de cookies</h2>
  <p>Nous nous réservons le droit de modifier cette politique de cookies à tout moment. Toute modification sera affichée sur cette page avec une indication de la date d'entrée en vigueur. Votre utilisation continue de notre site web après la publication des modifications constitue votre acceptation de cette politique de cookies modifiée.</p>

