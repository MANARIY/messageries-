  <h1>Politique de confidentialité</h1>
  <p> nous engageons à respecter votre vie privée et à protéger les informations personnelles que vous nous fournissez lorsque vous utilisez notre site web. Cette politique de confidentialité décrit comment nous collectons, utilisons, partageons et protégeons vos informations personnelles. Veuillez lire attentivement cette politique.</p>

  <h2>Collecte d'informations personnelles</h2>
  <p>Lorsque vous utilisez notre site web, il est possible que nous vous demandions de fournir certaines informations personnelles telles que votre nom, votre adresse e-mail, votre adresse postale, votre numéro de téléphone, etc. Nous collectons ces informations dans le but de vous fournir des services, de répondre à vos demandes, de vous informer sur nos activités et de vous contacter si nécessaire.</p>

  <h2>Utilisation des informations personnelles</h2>
  <p>Nous utilisons les informations personnelles que vous nous fournissez uniquement aux fins pour lesquelles elles ont été collectées. Cela peut inclure la communication avec vous, la fourniture des services demandés, l'amélioration de notre site web et de nos services, ainsi que le respect de nos obligations légales. Nous ne vendons ni ne louons vos informations personnelles à des tiers.</p>

  <h2>Divulgation des informations personnelles</h2>
  <p>Nous ne divulguons vos informations personnelles qu'à un nombre limité de tiers, dans les cas suivants :</p>
  <ul>
    <li>Lorsque cela est nécessaire pour fournir les services demandés ou pour répondre à vos demandes.</li>
    <li>Lorsque cela est requis par la loi ou dans le cadre d'une procédure légale.</li>
    <li>Lorsque cela est nécessaire pour protéger nos droits, notre sécurité ou notre propriété, ainsi que ceux de nos utilisateurs ou du public.</li>
  </ul>

  <h2>Sécurité des informations personnelles</h2>
  <p>Nous prenons des mesures de sécurité appropriées pour protéger vos informations personnelles contre tout accès non autorisé, toute utilisation abusive, toute divulgation ou toute destruction. Nous utilisons des protocoles de sécurité et des technologies de cryptage pour assurer la confidentialité de vos informations. Cependant, veuillez noter qu'aucune méthode de transmission sur Internet ou de stockage électronique n'est totalement sécurisée.</p>

  <h2>Cookies et technologies similaires</h2>
  <p>Nous utilisons des cookies et d'autres technologies similaires sur notre site web pour améliorer votre expérience utilisateur, analyser les tendances, administrer le site et recueillir des informations démographiques sur nos utilisateurs de manière agrégée. Vous pouvez contrôler l'utilisation des cookies via les paramètres de votre navigateur, mais veuillez noter que cela pourrait affecter certaines fonctionnalités de notre site web.</p>

  <h2>Liens vers des sites tiers</h2>
  <p>Notre site web peut contenir des liens vers d'autres sites web gérés par des tiers. Nous ne sommes pas responsables de la collecte, de l'utilisation et de la divulgation de vos informations personnelles par ces sites. Nous vous encourageons à lire les politiques de confidentialité de ces sites tiers avant de fournir vos informations personnelles.</p>

  <h2>Modifications de la politique de confidentialité</h2>
  <p>Nous nous réservons le droit de modifier cette politique de confidentialité à tout moment. Toute modification sera publiée sur cette page avec une indication de la date d'entrée en vigueur. Nous vous encourageons à consulter régulièrement cette politique pour rester informé des changements éventuels.</p>

  <h2>Contactez-nous</h2>
  <p>Si vous avez des questions, des préoccupations ou des demandes concernant notre politique de confidentialité ou nos pratiques en matière de confidentialité, veuillez nous contacter à severinmanariyo810gmail.com</p>
